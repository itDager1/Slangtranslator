export default function App() {
  const [searchValue, setSearchValue] = useState("");
  const [selectedCategory, setSelectedCategory] = useState("Интернет");
  const [selectedWord, setSelectedWord] = useState<string | null>(null);
  const [translation, setTranslation] = useState<DictionaryEntry | null>(null);
  const [translationMode, setTranslationMode] = useState<TranslationMode>("slangToRussian");
  const [history, setHistory] = useState<HistoryEntry[]>([]);
  const [favorites, setFavorites] = useState<FavoriteItem[]>([]);

  // Load history from localStorage on mount
  useEffect(() => {
    const savedHistory = localStorage.getItem('translationHistory');
    if (savedHistory) {
      try {
        setHistory(JSON.parse(savedHistory));
      } catch (e) {
        console.error('Failed to load history:', e);
      }
    }
  }, []);

  // Save history to localStorage whenever it changes
  useEffect(() => {
    localStorage.setItem('translationHistory', JSON.stringify(history));
  }, [history]);

  // Load favorites from localStorage on mount
  useEffect(() => {
    const savedFavorites = localStorage.getItem('translationFavorites');
    if (savedFavorites) {
      try {
        setFavorites(JSON.parse(savedFavorites));
      } catch (e) {
        console.error('Failed to load favorites:', e);
      }
    }
  }, []);

  // Save favorites to localStorage whenever they change
  useEffect(() => {
    localStorage.setItem('translationFavorites', JSON.stringify(favorites));
  }, [favorites]);

  // Add entry to history
  const addToHistory = (input: string, output: string) => {
    setHistory(prev => {
      // Check if this exact translation already exists
      const existingIndex = prev.findIndex(
        entry => entry.input.toLowerCase() === input.toLowerCase() && 
                 entry.mode === translationMode
      );
      
      // If exists, remove the old one and add new one at the top
      if (existingIndex !== -1) {
        const newHistory = [...prev];
        newHistory.splice(existingIndex, 1);
        return [{
          id: Date.now().toString(),
          input,
          output,
          mode: translationMode,
          timestamp: Date.now()
        }, ...newHistory].slice(0, 50);
      }
      
      // If doesn't exist, just add to the top
      return [{
        id: Date.now().toString(),
        input,
        output,
        mode: translationMode,
        timestamp: Date.now()
      }, ...prev].slice(0, 50);
    });
  };

  // Clear history
  const clearHistory = () => {
    setHistory([]);
  };

  // Handle selecting entry from history
  const handleHistorySelect = (entry: HistoryEntry) => {
    setTranslationMode(entry.mode);
    setSearchValue(entry.input);
    setTimeout(() => handleSearch(entry.input), 0);
  };

  // Add to favorites
  const addToFavorites = (word: string, translationText: string, category?: string) => {
    const newFavorite: FavoriteItem = {
      id: Date.now().toString(),
      word,
      translation: translationText,
      category,
      timestamp: Date.now()
    };
    
    setFavorites(prev => {
      // Check if already exists
      const exists = prev.some(fav => 
        fav.word.toLowerCase() === word.toLowerCase()
      );
      
      if (exists) {
        return prev;
      }
      
      return [newFavorite, ...prev];
    });
  };

  // Remove from favorites
  const removeFromFavorites = (id: string) => {
    setFavorites(prev => prev.filter(fav => fav.id !== id));
  };

  // Check if word is in favorites
  const isFavorite = (word: string): boolean => {
    return favorites.some(fav => fav.word.toLowerCase() === word.toLowerCase());
  };

  // Toggle favorite
  const toggleFavorite = () => {
    if (!selectedWord || !translation) return;
    
    if (isFavorite(selectedWord)) {
      const favorite = favorites.find(fav => fav.word.toLowerCase() === selectedWord.toLowerCase());
      if (favorite) {
        removeFromFavorites(favorite.id);
      }
    } else {
      const translationText = translation.shortTranslation || translation.definition;
      addToFavorites(selectedWord, translationText, translation.category);
    }
  };

  // Helper function to match case of the first letter
  const matchCase = (original: string, translated: string): string => {
    if (!original || !translated) return translated;
    
    const firstChar = original.charAt(0);
    const isUpperCase = firstChar === firstChar.toUpperCase() && firstChar !== firstChar.toLowerCase();
    
    if (isUpperCase) {
      return translated.charAt(0).toUpperCase() + translated.slice(1);
    } else {
      return translated.charAt(0).toLowerCase() + translated.slice(1);
    }
  };

  // Helper function to find base form of a word
  const findBaseForm = (word: string): string => {
    const lowerWord = word.toLowerCase();
    // Check if it's already a base form or if there's a mapping to base form
    return wordFormsMap[lowerWord] || lowerWord;
  };

  const handleSearch = (text: string) => {
    const currentDictionary = translationMode === "slangToRussian" ? dictionary : reverseDictionary;
    
    // Split text into words, preserving punctuation
    const words = text.split(/(\s+|[.,!?;:—\-«»"„…]+)/);
    
    let translatedText = "";
    let foundWords: string[] = [];
    let wordExplanations: Array<{ word: string; explanation: string }> = [];
    
    // Process each word
    words.forEach(token => {
      const lowerToken = token.toLowerCase().trim();
      
      if (lowerToken) {
        // First try direct match
        let entry = currentDictionary[lowerToken];
        let baseForm = lowerToken;
        
        // If not found, try to find base form
        if (!entry) {
          baseForm = findBaseForm(lowerToken);
          entry = currentDictionary[baseForm];
        }
        
        // If we found a translation
        if (entry) {
          // Use shortTranslation for single word replacement, otherwise use definition
          const translationText = entry.shortTranslation || entry.definition;
          // Match the case of the original word
          const casedTranslation = matchCase(token.trim(), translationText);
          translatedText += casedTranslation;
          foundWords.push(lowerToken);
          
          // Store explanation for slang words (when translating from slang to Russian)
          if (translationMode === "slangToRussian" && entry.definition) {
            wordExplanations.push({
              word: token.trim(),
              explanation: entry.definition
            });
          }
        } else {
          // Keep original token if no translation found
          translatedText += token;
        }
      } else {
        // Keep spaces and punctuation
        translatedText += token;
      }
    });
    
    // If we found at least one word, show translated sentence
    if (foundWords.length > 0) {
      setSelectedWord(text);
      setTranslation({
        word: text,
        definition: translatedText,
        examples: [`Исходный текст: ${text}`],
        category: "Перевод предложения",
        explanations: wordExplanations.length > 0 ? wordExplanations : undefined
      });
      // Add to history
      addToHistory(text, translatedText);
    } else {
      // Single word lookup for backwards compatibility
      const wordKey = text.toLowerCase().trim();
      const baseForm = findBaseForm(wordKey);
      const found = currentDictionary[baseForm];
      
      if (found) {
        setSelectedWord(text);
        setTranslation(found);
        // Add to history - use shortTranslation or definition
        addToHistory(text, found.shortTranslation || found.definition);
      } else {
        setSelectedWord(text);
        setTranslation({
          word: text,
          definition: "Извините, в этом тексте не найдено слов из нашего словаря. Попробуйте другие слова из популярных.",
          examples: [],
          category: ""
        });
      }
    }
  };
  
  const toggleTranslationMode = () => {
    // Сохраняем текущие значения полей
    const currentInput = searchValue;
    const currentOutput = translation?.definition || "";
    
    // Переключаем режим перевода
    setTranslationMode(prev => prev === "slangToRussian" ? "russianToSlang" : "slangToRussian");
    
    // Меняем местами значения: выход становится входом
    if (currentOutput && 
        currentOutput !== "Извините, в этом тексте не найдено слов из нашего словаря. Попробуйте другие слова из популярных.") {
      setSearchValue(currentOutput);
      // Используем setTimeout, чтобы дать React время обновить состояние translationMode
      // и затем выполнить перевод с новым режимом
      setTimeout(() => handleSearch(currentOutput), 0);
    } else if (currentInput) {
      // Если нет перевода, оставляем текущий ввод
      setSearchValue(currentInput);
      setSelectedWord(null);
      setTranslation(null);
    } else {
      // Если оба поля пусты, просто очищаем
      setSearchValue("");
      setSelectedWord(null);
      setTranslation(null);
    }
  };

  const handleSelectWord = (word: string) => {
    setSearchValue(word);
    handleSearch(word);
  };

  return (
    <div className="min-h-screen bg-gray-50 flex flex-col">
      <Navbar 
        history={history}
        onClearHistory={clearHistory}
        onSelectEntry={handleHistorySelect}
      />
      
      <main className="flex-1">
        <Header translationMode={translationMode} />
        
        <div className="max-w-6xl mx-auto px-4 mb-8">
          {/* Language selector header */}
          <div className="mb-4 flex items-center justify-center gap-4">
            <div className="bg-gray-900 border border-gray-700 rounded-lg px-4 py-3 flex items-center justify-between w-36">
              <span className="text-[rgb(255,255,255)] uppercase font-normal text-[16px]">{translationMode === "slangToRussian" ? "Сленг" : "текст"}</span>
            </div>
            
            <Button 
              variant="ghost" 
              size="icon"
              onClick={toggleTranslationMode}
              className="rounded-full hover:bg-gray-100"
            >
              <ArrowRightLeft className="w-5 h-5 text-gray-600" />
            </Button>
            
            <div className="bg-gray-900 border border-gray-700 rounded-lg px-4 py-3 flex items-center justify-between w-36">
              <span className="text-[rgb(255,255,255)] font-[Mako] uppercase">{translationMode === "slangToRussian" ? "текст" : "Сленг"}</span>
            </div>
          </div>
          
          {/* Translation areas */}
          <div className="grid grid-cols-1 lg:grid-cols-2 gap-4">
            <SearchBar
              value={searchValue}
              onChange={setSearchValue}
              onSearch={handleSearch}
              translationMode={translationMode}
            />
            
            <TranslationDisplay 
              selectedWord={selectedWord} 
              translation={translation}
              translationMode={translationMode}
              isFavorite={selectedWord ? isFavorite(selectedWord) : false}
              onToggleFavorite={toggleFavorite}
            />
          </div>
        </div>
        
        <FavoriteTranslations
          favorites={favorites}
          onRemove={removeFromFavorites}
          onSelect={handleSelectWord}
        />
        
        <CategorySlider
          selectedCategory={selectedCategory}
          onSelectCategory={setSelectedCategory}
        />
        
        <div className="max-w-4xl mx-auto px-4 mb-8">
          <PopularWords 
            onSelectWord={handleSelectWord} 
            selectedWord={selectedWord}
            translationMode={translationMode}
            selectedCategory={selectedCategory}
          />
        </div>
        
        <div className="max-w-6xl mx-auto px-4 mb-8">
          <WordsList 
            category={selectedCategory} 
            onSelectWord={handleSelectWord}
          />
        </div>
        
        <Stats />
      </main>
      
      <Footer />
      <Toaster 
        position="top-center"
        toastOptions={{
          style: {
            background: '#22c55e',
            color: '#ffffff',
            border: 'none',
          },
        }}
      />
    </div>
  );
}
