  /**
   * Функция для применения правильной формы к переведенному слову
   * @param originalWord - исходное слово со сленга
   * @param translatedBase - базовая форма перевода
   * @returns переведенное слово в правильной форме
   */
  const applyCorrectForm = (originalWord: string, translatedBase: string): string => {
    const cleanOriginal = originalWord.toLowerCase().replace(/[.,!?;:()\"\"\"«»]/g, '');
    const lowerTranslated = translatedBase.toLowerCase();
    
    // Проверяем, есть ли словарь форм для этого переведенного слова
    if (translatedWordForms[lowerTranslated]) {
      const form = translatedWordForms[lowerTranslated][cleanOriginal];
      if (form) {
        // Сохраняем регистр первой буквы исходного слова
        return matchCase(originalWord, form);
      }
    }
    
    // Если нет специальной формы, возвращаем базовую форму с правильным регистром
    return matchCase(originalWord, translatedBase);
  };

  // Helper function to match case of the first letter
  const matchCase = (original: string, translated: string): string => {
    if (!original || !translated) return translated;
    
    const firstChar = original.charAt(0);
    const isUpperCase = firstChar === firstChar.toUpperCase() && firstChar !== firstChar.toLowerCase();
    
    if (isUpperCase) {
      return translated.charAt(0).toUpperCase() + translated.slice(1);
    } else {
      return translated.charAt(0).toLowerCase() + translated.slice(1);
    }
  };

  // Helper function to find base form of a word
  const findBaseForm = (word: string): string => {
    const lowerWord = word.toLowerCase();
    // Check if it's already a base form or if there's a mapping to base form
    return wordFormsMap[lowerWord] || lowerWord;
  };

  const handleSearch = (text: string) => {
    if (!text.trim()) {
      setSelectedWord(null);
      setTranslation(null);
      return;
    }
    
    // Use local dictionary for translation
    const currentDictionary = translationMode === "slangToRussian" ? dictionary : reverseDictionary;
    const inputText = text.trim();
    
    // Try to find direct match first
    const lowerText = inputText.toLowerCase();
    const baseForm = findBaseForm(lowerText);
    const directMatch = currentDictionary[lowerText] || currentDictionary[baseForm];
    
    if (directMatch) {
      // Found single word translation
      setSelectedWord(inputText);
      setTranslation(directMatch);
      addToHistory(inputText, directMatch.shortTranslation || directMatch.definition);
      return;
    }
    
    // Try sentence translation
    const words = inputText.split(/\s+/);
    let hasTranslation = false;
    let translatedWords: string[] = [];
    let explanations: Array<{ word: string; explanation: string }> = [];
    
    for (const word of words) {
      const cleanWord = word.toLowerCase().replace(/[.,!?;:()"""«»]/g, '');
      const baseForm = findBaseForm(cleanWord);
      const found = currentDictionary[cleanWord] || currentDictionary[baseForm];
      
      if (found) {
        hasTranslation = true;
        const translation = found.shortTranslation || found.definition.split(',')[0].trim();
        
        // Применяем правильную форму к переведенному слову
        const correctForm = applyCorrectForm(cleanWord, translation);
        translatedWords.push(matchCase(word, correctForm));
        
        // Add explanation for slang words
        if (translationMode === "slangToRussian") {
          explanations.push({
            word: cleanWord,
            explanation: found.definition
          });
        }
      } else {
        translatedWords.push(word);
      }
    }
    
    if (hasTranslation) {
      const translatedText = translatedWords.join(' ');
      setSelectedWord(inputText);
      setTranslation({
        word: inputText,
        definition: translatedText,
        examples: [`Исходный текст: ${inputText}`],
        category: "Перевод",
        explanations: explanations.length > 0 ? explanations : undefined
      });
      addToHistory(inputText, translatedText);
    } else {
      setSelectedWord(inputText);
      setTranslation({
        word: inputText,
        definition: "Извините, в этом тексте не найдено слов из нашего словаря. Попробуйте другие слова из популярных.",
        examples: [],
        category: ""
      });
    }
  };
  
  const toggleTranslationMode = () => {
    // Сохраняем текущие значения полей
    const currentInput = searchValue;
    const currentOutput = translation?.definition || "";
    
    // Переключаем режим перевода
    setTranslationMode(prev => prev === "slangToRussian" ? "russianToSlang" : "slangToRussian");
    
    // Меняем местами значения: выход становится входом
    if (currentOutput && 
        currentOutput !== "Извините, в этом тексте не найдено слов из нашего словаря. Попробуйте другие слова из популярных.") {
      setSearchValue(currentOutput);
      // Используем setTimeout, чтобы дать React время обновить состояние translationMode
      // и затем выполнить перевод с новым режимом
      setTimeout(() => handleSearch(currentOutput), 0);
    } else if (currentInput) {
      // Если нет перевода, оставляем текущий ввод
      setSearchValue(currentInput);
      setSelectedWord(null);
      setTranslation(null);
    } else {
      // Если оба поля пусты, просто очищаем
      setSearchValue("");
      setSelectedWord(null);
      setTranslation(null);
    }
  };

  const handleSelectWord = (word: string) => {
    setSearchValue(word);
    handleSearch(word);
  };

  return (
    <div className="min-h-screen bg-gray-50 flex flex-col">
      <Navbar 
        history={history}
        onClearHistory={clearHistory}
        onSelectEntry={handleHistorySelect}
      />
      
      <main className="flex-1">
        <Header translationMode={translationMode} />
        
        <div className="max-w-6xl mx-auto px-4 mb-8">
          {/* Language selector header */}
          <div className="mb-4 flex items-center justify-center gap-4">
            <div className="bg-yellow-600 border border-yellow-500 rounded-lg px-4 py-3 flex items-center justify-between w-36">
              <span className="text-[rgb(255,255,255)] uppercase font-normal text-[16px]">{translationMode === "slangToRussian" ? "Сленг" : "текст"}</span>
            </div>
            
            <Button 
              variant="ghost" 
              size="icon"
              onClick={toggleTranslationMode}
              className="rounded-full hover:bg-gray-100"
            >
              <div className="w-8 h-8 rounded-full bg-[rgb(255,215,0)] hover:opacity-60 active:bg-[rgb(147,220,53)] transition-all duration-150 flex items-center justify-center">
                <ArrowRightLeft className="w-5 h-5 text-gray-600" />
              </div>
            </Button>
            
            <div className="bg-yellow-600 border border-yellow-500 rounded-lg px-4 py-3 flex items-center justify-between w-36">
              <span className="text-[rgb(255,255,255)] font-[Mako] uppercase">{translationMode === "slangToRussian" ? "текст" : "Сленг"}</span>
            </div>
          </div>
          
          {/* Translation areas */}
          <div className="grid grid-cols-1 lg:grid-cols-2 gap-4">
            <SearchBar
              value={searchValue}
              onChange={setSearchValue}
              onSearch={handleSearch}
              translationMode={translationMode}
            />
            
            <TranslationDisplay 
              selectedWord={selectedWord} 
              translation={translation}
              translationMode={translationMode}
              isFavorite={selectedWord ? isFavorite(selectedWord) : false}
              onToggleFavorite={toggleFavorite}
            />
          </div>
        </div>
        
        <FavoriteTranslations
          favorites={favorites}
          onRemove={removeFromFavorites}
          onSelect={handleSelectWord}
        />
        
        <CategorySlider
          selectedCategory={selectedCategory}
          onSelectCategory={setSelectedCategory}
        />
        
        <div className="max-w-4xl mx-auto px-4 mb-8">
          <PopularWords 
            onSelectWord={handleSelectWord} 
            selectedWord={selectedWord}
            translationMode={translationMode}
            selectedCategory={selectedCategory}
          />
        </div>
        
        <div className="max-w-6xl mx-auto px-4 mb-8">
          <WordsList 
            category={selectedCategory} 
            onSelectWord={handleSelectWord}
          />
        </div>
        
        <Stats />
      </main>
      
      <Footer />
      <Toaster 
        position="top-center"
        toastOptions={{
          style: {
            background: '#22c55e',
            color: '#ffffff',
            border: 'none',
          },
        }}
      />
    </div>
  );
